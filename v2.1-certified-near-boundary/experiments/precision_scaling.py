#!/usr/bin/env python3
"""Generate fast precision-scaling diagnostics.

This script reports the planned/retained depth scaling used in the manuscript.
For high D it avoids recomputing full high-precision values so the release smoke
run remains lightweight.
"""
import csv
import math
import sys
from pathlib import Path
import mpmath as mp

ROOT = Path(__file__).resolve().parents[1]
sys.path.insert(0, str(ROOT / "src"))

from certified_gaussian_theta import plan_depths

M_VALUES = ["1e-4", "1", "100"]
D_VALUES = [1000, 5000, 10000]


def main() -> None:
    out = ROOT / "results" / "precision_scaling.csv"
    out.parent.mkdir(exist_ok=True)
    rows = []
    for m_str in M_VALUES:
        m = mp.mpf(m_str)
        for D in D_VALUES:
            N_dir, N_tr = plan_depths(m, D, 50)
            selected = "transformed" if N_tr < N_dir else "direct"
            N = min(N_dir, N_tr)
            rows.append({
                "m": m_str,
                "requested_digits": D,
                "planned_direct_depth": N_dir,
                "planned_transformed_depth": N_tr,
                "selected_regime_by_depth": selected,
                "retained_depth_proxy": N,
                "N_over_sqrt_D": round(N / math.sqrt(D), 6),
                "note": "depth-planning diagnostic; full certificates generated by regime_selection scripts",
            })
    with out.open("w", newline="") as f:
        writer = csv.DictWriter(f, fieldnames=list(rows[0].keys()))
        writer.writeheader()
        writer.writerows(rows)
    print(f"wrote {out}")


if __name__ == "__main__":
    main()
